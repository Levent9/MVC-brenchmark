"""UniSpeaker MVC benchmark evaluation.

Implements the objective metrics from the paper "UniSpeaker: A Unified Approach
for Multimodality-driven Speaker Generation" (Sheng et al., EMNLP 2025).

Three voice dimensions are evaluated:

* Voice suitability:
    - SST (Speaker Similarity with Target): cosine similarity between the
      generated speech and a recording of the *target speaker* (`target_wav`),
      which the inference model never sees.
    - SSC (Speaker Similarity Consistency): mean pairwise cosine similarity
      between generated speech produced from *different inputs of the same
      speaker* (e.g. multiple face crops of one person). Higher = more robust.
* Voice diversity:
    - SSD (Speaker Similarity Diversity): mean pairwise cosine similarity
      between generated speech produced from *different speakers*. Lower
      means a richer voice space.
* Speech quality:
    - WER: word error rate from whisper-large-v3 (paraformer-zh for zh).
      For TTS we use the supplied transcript `text`; for VC / AVE the
      transcript is generated by transcribing `source_wav` so that we
      check content preservation rather than relying on a missing label.
    - MOS-Match / MOS-Nat are subjective and not implemented here.

Manifest format (jsonl, one record per generated utterance). All wav fields
are optional — populate the ones the task supplies:

    {
        "utt":        "<generated wav stem; must match files in --gen_dir>",
        "spk":        "<speaker id; SSC pools within, SSD pools across>",
        "target_wav": "<absolute path to target/reference speech>",  // TTS+VC
        "source_wav": "<absolute path to source speech>",            // VC+AVE
        "text":       "<reference transcript>",                      // TTS only
        "task":       "face_tts | face_vc | text_tts | text_vc | ave"  // optional
    }

Per-task expectation:

    face_tts / text_tts: target_wav + text
    face_vc  / text_vc : target_wav + source_wav
    ave                : source_wav (SST anchors on it; WER from ASR(source_wav))
"""

from __future__ import annotations

import argparse
import json
import logging
import os
import sys
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path
from typing import Callable, Dict, List, Optional

import numpy as np
from tqdm import tqdm

sys.path.append(str(Path(__file__).resolve().parent))
from spk_embedding import CamPPlusExtractor, cosine_sim, cosine_sim_matrix


@dataclass
class Item:
    utt: str
    spk: str
    text: Optional[str]
    target_wav: Optional[str]
    source_wav: Optional[str]
    task: Optional[str]
    gen_wav: str

    def sst_anchor(self) -> Optional[str]:
        """Recording of the target speaker used as the SST cosine-sim partner.

        For every task the SST anchor is `target_wav` — a recording of the
        target speaker that the inference model never sees. For AVE the
        target speaker is the source speaker, so `target_wav` is a *different
        utterance* of that same speaker, not the source itself (cos(gen,
        source) would otherwise be inflated by residual content / prosody).
        Falls back to `source_wav` only if `target_wav` is missing.
        """
        if self.target_wav:
            return self.target_wav
        if self.source_wav:
            return self.source_wav
        return None


# Pipe-delimited .lst column schemas for each MVC task.
# Column 1 is always the utterance id (must match `<gen_dir>/<utt>.wav`).
# Columns prefixed with `_` are consumed at inference but ignored by the
# evaluator (face image / text description / attribute prompt / …).
#
# All five tasks share the same 4-column layout:
#   TTS-class (face_tts, text_tts): wav_id | tgt_wav | <condition> | text
#   VC -class (face_vc, text_vc, ave): wav_id | tgt_wav | <condition> | source_wav


LST_SCHEMAS: Dict[str, List[List[str]]] = {
    "face_tts": [["utt", "target_wav", "_image",       "text"]],
    "face_vc":  [["utt", "target_wav", "_image",       "source_wav"]],
    "text_tts": [["utt", "target_wav", "_description", "text"]],
    "text_vc":  [["utt", "target_wav", "_description", "source_wav"]],
    "ave":      [["utt", "target_wav", "_attribute",   "source_wav"]],
}


def _default_spk(utt: str) -> str:
    """LRS3-style utt id `<video_id>_<utt_idx>` → `<video_id>` as the speaker."""
    return utt.rsplit("_", 1)[0] if "_" in utt else utt


def _resolve(path: Optional[str], root: Optional[str]) -> Optional[str]:
    if not path:
        return None
    if root and not os.path.isabs(path):
        return os.path.join(root, path)
    return path


def load_manifest(
    manifest_path: str,
    gen_dir: str,
    task: Optional[str] = None,
    data_root: Optional[str] = None,
) -> List[Item]:
    """Load the manifest. Supports two formats:

    * `.jsonl` — one JSON record per line, fields documented in the module docstring.
    * `.lst`   — pipe-delimited; columns are interpreted by `LST_SCHEMAS[task]`.
                 `--task` is required for `.lst` inputs and `--data_root` is
                 prepended to any relative wav path inside the file.
    """
    ext = os.path.splitext(manifest_path)[1].lower()
    items: List[Item] = []
    missing = 0

    if ext == ".lst":
        if not task or task not in LST_SCHEMAS:
            raise ValueError(
                f".lst input requires --task one of {list(LST_SCHEMAS)}; got {task!r}"
            )
        candidate_schemas = LST_SCHEMAS[task]
        with open(manifest_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                cols = line.split("|")
                schema = next((s for s in candidate_schemas if len(s) == len(cols)), None)
                if schema is None:
                    expected = " or ".join(str(len(s)) for s in candidate_schemas)
                    logging.warning("Bad row (expected %s cols): %s", expected, line)
                    continue
                rec = dict(zip(schema, cols))
                utt = rec["utt"]
                gen_wav = os.path.join(gen_dir, f"{utt}.wav")
                if not os.path.isfile(gen_wav):
                    missing += 1
                    continue
                items.append(
                    Item(
                        utt=utt,
                        spk=_default_spk(utt),
                        text=rec.get("text") or None,
                        target_wav=_resolve(rec.get("target_wav"), data_root),
                        source_wav=_resolve(rec.get("source_wav"), data_root),
                        task=task,
                        gen_wav=gen_wav,
                    )
                )
    else:
        with open(manifest_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                rec = json.loads(line)
                utt = rec["utt"]
                gen_wav = os.path.join(gen_dir, f"{utt}.wav")
                if not os.path.isfile(gen_wav):
                    missing += 1
                    continue
                items.append(
                    Item(
                        utt=utt,
                        spk=rec.get("spk") or _default_spk(utt),
                        text=rec.get("text") or None,
                        target_wav=_resolve(rec.get("target_wav"), data_root),
                        source_wav=_resolve(rec.get("source_wav"), data_root),
                        task=rec.get("task") or task,
                        gen_wav=gen_wav,
                    )
                )

    if missing:
        logging.warning("Skipped %d manifest rows with no matching wav in %s", missing, gen_dir)
    if not items:
        raise FileNotFoundError(f"No generated wavs matched manifest entries in {gen_dir}")
    return items


def build_embeddings(items: List[Item], extractor: CamPPlusExtractor) -> Dict[str, np.ndarray]:
    cache: Dict[str, np.ndarray] = {}
    wavs = {it.gen_wav for it in items}
    for it in items:
        anchor = it.sst_anchor()
        if anchor:
            wavs.add(anchor)
    for path in tqdm(sorted(wavs), desc="Extracting embeddings"):
        try:
            cache[path] = extractor.embed(path)
        except Exception as exc:
            logging.warning("Failed to embed %s: %s", path, exc)
    return cache


def compute_sst(items: List[Item], emb: Dict[str, np.ndarray]) -> Dict[str, float]:
    per_utt: Dict[str, float] = {}
    for it in items:
        anchor = it.sst_anchor()
        if not anchor:
            continue
        if it.gen_wav not in emb or anchor not in emb:
            continue
        per_utt[it.utt] = cosine_sim(emb[it.gen_wav], emb[anchor])
    return per_utt


def _mean_offdiag(sim: np.ndarray) -> float:
    n = sim.shape[0]
    if n < 2:
        return float("nan")
    mask = ~np.eye(n, dtype=bool)
    return float(sim[mask].mean())


def compute_ssc(items: List[Item], emb: Dict[str, np.ndarray]) -> Dict[str, float]:
    """Mean pairwise cosine similarity within each speaker group."""
    by_spk: Dict[str, List[np.ndarray]] = defaultdict(list)
    for it in items:
        if it.gen_wav in emb:
            by_spk[it.spk].append(emb[it.gen_wav])
    per_spk: Dict[str, float] = {}
    for spk, vecs in by_spk.items():
        if len(vecs) < 2:
            continue
        sim = cosine_sim_matrix(np.stack(vecs))
        per_spk[spk] = _mean_offdiag(sim)
    return per_spk


def compute_ssd(items: List[Item], emb: Dict[str, np.ndarray]) -> float:
    """Mean cross-speaker pairwise cosine similarity.

    Lower = more diverse. We collapse each speaker to its mean embedding so
    SSD reflects between-speaker geometry, not within-speaker noise.
    """
    by_spk: Dict[str, List[np.ndarray]] = defaultdict(list)
    for it in items:
        if it.gen_wav in emb:
            by_spk[it.spk].append(emb[it.gen_wav])
    if len(by_spk) < 2:
        return float("nan")
    centroids = np.stack([np.mean(np.stack(v), axis=0) for v in by_spk.values()])
    sim = cosine_sim_matrix(centroids)
    return _mean_offdiag(sim)


# Default ASR checkpoints — public, auto-downloaded by Transformers / FunASR.
# Override via --asr_model on the CLI or by passing a local path.
DEFAULT_WHISPER_ID = "openai/whisper-large-v3"
DEFAULT_PARAFORMER_ID = "paraformer-zh"


def _build_transcriber(lang: str, device: str, asr_model: Optional[str]) -> Callable[[str], str]:
    """Return a transcribe(path)->str callable matching the paper's ASR setup."""
    if lang == "en":
        import scipy.signal
        import soundfile as sf
        from transformers import WhisperForConditionalGeneration, WhisperProcessor

        model_id = asr_model or DEFAULT_WHISPER_ID
        processor = WhisperProcessor.from_pretrained(model_id)
        model = WhisperForConditionalGeneration.from_pretrained(model_id).to(device)
        forced_decoder_ids = processor.get_decoder_prompt_ids(language="english", task="transcribe")

        def transcribe(path: str) -> str:
            wav, sr = sf.read(path)
            if sr != 16000:
                wav = scipy.signal.resample(wav, int(len(wav) * 16000 / sr))
            inputs = processor(wav, sampling_rate=16000, return_tensors="pt").input_features.to(device)
            ids = model.generate(inputs, forced_decoder_ids=forced_decoder_ids)
            return processor.batch_decode(ids, skip_special_tokens=True)[0]

        return transcribe

    if lang == "zh":
        import zhconv
        from funasr import AutoModel

        model_id = asr_model or DEFAULT_PARAFORMER_ID
        model = AutoModel(model=model_id, disable_update=True)

        def transcribe(path: str) -> str:
            res = model.generate(input=path, batch_size_s=300, disable_pbar=True)
            return zhconv.convert(res[0]["text"], "zh-cn")

        return transcribe

    raise ValueError(f"Unsupported language: {lang}")


def _normalizer(lang: str) -> Callable[[str], str]:
    import string

    from zhon.hanzi import punctuation as zh_punct

    punctuation_all = zh_punct + string.punctuation

    def norm(s: str) -> str:
        for ch in punctuation_all:
            if ch != "'":
                s = s.replace(ch, "")
        s = s.replace("  ", " ")
        if lang == "zh":
            return " ".join(list(s))
        return s.lower()

    return norm


def compute_wer(
    items: List[Item], lang: str, device: str, asr_model: Optional[str] = None
) -> Dict[str, Dict[str, float]]:
    """WER per utterance.

    Reference text is `item.text` for TTS, otherwise ASR(`source_wav`) for VC/AVE.
    Hypothesis is always ASR(`gen_wav`). The same ASR model is used on both
    sides so any systematic ASR bias cancels out for VC/AVE comparisons.
    """
    from jiwer import compute_measures

    transcribe = _build_transcriber(lang, device, asr_model)
    norm = _normalizer(lang)
    src_cache: Dict[str, str] = {}

    per_utt: Dict[str, Dict[str, float]] = {}
    for it in tqdm(items, desc="WER"):
        if it.text:
            ref = it.text
        elif it.source_wav:
            if it.source_wav not in src_cache:
                try:
                    src_cache[it.source_wav] = transcribe(it.source_wav)
                except Exception as exc:
                    logging.warning("ASR failed on source %s: %s", it.source_wav, exc)
                    src_cache[it.source_wav] = ""
            ref = src_cache[it.source_wav]
        else:
            continue
        if not ref:
            continue
        try:
            hypo = transcribe(it.gen_wav)
        except Exception as exc:
            logging.warning("ASR failed on gen %s: %s", it.gen_wav, exc)
            continue
        m = compute_measures(norm(ref), norm(hypo))
        ref_tokens = norm(ref).split(" ")
        ref_len = max(1, len([t for t in ref_tokens if t]))
        per_utt[it.utt] = {
            "wer": float(m["wer"]),
            "ref": ref,
            "hypo": hypo,
            "ref_len": ref_len,
            "subs": int(m["substitutions"]),
            "dele": int(m["deletions"]),
            "inse": int(m["insertions"]),
            "ref_source": "text" if it.text else "asr_source_wav",
        }
    return per_utt


def write_report(outdir: str, results: dict) -> None:
    os.makedirs(outdir, exist_ok=True)

    if "sst" in results:
        with open(os.path.join(outdir, "utt2sst.txt"), "w") as f:
            for utt, v in sorted(results["sst"].items()):
                f.write(f"{utt} {v:.4f}\n")

    if "ssc" in results:
        with open(os.path.join(outdir, "spk2ssc.txt"), "w") as f:
            for spk, v in sorted(results["ssc"].items()):
                f.write(f"{spk} {v:.4f}\n")

    if "wer" in results:
        with open(os.path.join(outdir, "utt2wer.txt"), "w") as f:
            f.write("utt\twer\tref_source\tref\thypo\n")
            for utt, info in sorted(results["wer"].items()):
                f.write(
                    f"{utt}\t{info['wer']*100:.2f}%\t{info['ref_source']}\t{info['ref']}\t{info['hypo']}\n"
                )

    summary = results.get("summary", {})
    with open(os.path.join(outdir, "summary.json"), "w") as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)

    lines = ["UniSpeaker MVC benchmark — objective evaluation"]
    for k, v in summary.items():
        if isinstance(v, float):
            lines.append(f"  {k:18s} = {v:.4f}")
        else:
            lines.append(f"  {k:18s} = {v}")
    print("\n".join(lines))
    with open(os.path.join(outdir, "summary.txt"), "w") as f:
        f.write("\n".join(lines) + "\n")


def get_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description="UniSpeaker MVC benchmark evaluator")
    p.add_argument("--gen_dir", required=True, help="Directory of generated .wav files")
    p.add_argument(
        "--manifest",
        required=True,
        help="Path to manifest. Either a .jsonl (free schema, see header docstring) "
        "or one of the bundled .lst files (face_tts / face_vc / text_tts / text_vc / ave).",
    )
    p.add_argument(
        "--task",
        default=None,
        choices=list(LST_SCHEMAS),
        help="Required when --manifest is a .lst; selects column semantics.",
    )
    p.add_argument(
        "--data_root",
        default=None,
        help="Prefix prepended to any relative wav/image path found in --manifest.",
    )
    p.add_argument("--outdir", required=True, help="Where to write per-utt and summary results")
    p.add_argument(
        "--tasks",
        nargs="+",
        default=["sst", "ssc", "ssd"],
        choices=["sst", "ssc", "ssd", "wer"],
        help="Which metrics to compute. Default: sst ssc ssd",
    )
    p.add_argument(
        "--spk_onnx",
        required=True,
        help=(
            "Path to the CAM++ speaker-verification ONNX model "
            "(3D-Speaker, footnote 2 of the paper). "
            "Download from https://modelscope.cn/models/iic/speech_campplus_sv_zh-cn_16k-common "
            "or export the ONNX via the 3D-Speaker repo: https://github.com/modelscope/3D-Speaker"
        ),
    )
    p.add_argument("--lang", default="en", choices=["en", "zh"], help="Language for WER")
    p.add_argument(
        "--asr_model",
        default=None,
        help=(
            "ASR model id or local path. Defaults: "
            f"en={DEFAULT_WHISPER_ID} (HuggingFace), "
            f"zh={DEFAULT_PARAFORMER_ID} (FunASR / ModelScope auto-download)."
        ),
    )
    p.add_argument("--device", default="cuda", help="Device for the ASR model")
    return p


def main() -> None:
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
    args = get_parser().parse_args()

    items = load_manifest(args.manifest, args.gen_dir, task=args.task, data_root=args.data_root)
    logging.info("Loaded %d utterances", len(items))

    summary: Dict[str, float] = {"n_utt": len(items)}
    results: Dict[str, dict] = {"summary": summary}

    needs_emb = any(t in args.tasks for t in ("sst", "ssc", "ssd"))
    if needs_emb:
        extractor = CamPPlusExtractor(args.spk_onnx)
        emb = build_embeddings(items, extractor)

        if "sst" in args.tasks:
            sst = compute_sst(items, emb)
            results["sst"] = sst
            if sst:
                summary["SST(%)"] = float(np.mean(list(sst.values()))) * 100
                summary["n_sst"] = len(sst)

        if "ssc" in args.tasks:
            ssc = compute_ssc(items, emb)
            results["ssc"] = ssc
            if ssc:
                summary["SSC(%)"] = float(np.mean(list(ssc.values()))) * 100
                summary["n_ssc_speakers"] = len(ssc)

        if "ssd" in args.tasks:
            ssd = compute_ssd(items, emb)
            results["ssd"] = ssd
            summary["SSD(%)"] = ssd * 100

    if "wer" in args.tasks:
        wer = compute_wer(items, args.lang, args.device, args.asr_model)
        results["wer"] = wer
        if wer:
            total_subs = sum(v["subs"] for v in wer.values())
            total_dele = sum(v["dele"] for v in wer.values())
            total_inse = sum(v["inse"] for v in wer.values())
            total_ref = sum(v["ref_len"] for v in wer.values())
            summary["WER(%)"] = (total_subs + total_dele + total_inse) / max(1, total_ref) * 100
            summary["mean_utt_WER(%)"] = float(np.mean([v["wer"] for v in wer.values()])) * 100
            summary["n_wer"] = len(wer)
            n_text = sum(1 for v in wer.values() if v["ref_source"] == "text")
            summary["wer_ref_text"] = n_text
            summary["wer_ref_asr_source"] = len(wer) - n_text

    write_report(args.outdir, results)


if __name__ == "__main__":
    main()
